let currentGallery = null;
let currentIndex = 0;

const gallery = document.getElementById("globalGallery");
const galleryImage = document.getElementById("galleryImage");
const galleryTitle = document.getElementById("galleryTitle");
const galleryText = document.getElementById("galleryText");
const closeBtn = document.querySelector(".gallery-close");
const prevBtn = document.querySelector(".gallery-prev");
const nextBtn = document.querySelector(".gallery-next");

function openGallery(galleryName, index = 0) {
  currentGallery = galleries[galleryName];

  if (!currentGallery) return;

  currentIndex = Number(index) || 0;
  updateGallery();

  gallery.classList.add("active");
  gallery.setAttribute("aria-hidden", "false");
  document.body.classList.add("gallery-open");
}

function closeGallery() {
  gallery.classList.remove("active");
  gallery.setAttribute("aria-hidden", "true");
  document.body.classList.remove("gallery-open");
}

function updateGallery() {
  const item = currentGallery[currentIndex];

  galleryImage.src = item.src;
  galleryImage.alt = item.title;
  galleryTitle.textContent = item.title;
  galleryText.textContent = item.text || "";
}

function nextImage() {
  currentIndex = (currentIndex + 1) % currentGallery.length;
  updateGallery();
}

function prevImage() {
  currentIndex = (currentIndex - 1 + currentGallery.length) % currentGallery.length;
  updateGallery();
}

document.querySelectorAll("[data-gallery]").forEach((item) => {
  item.addEventListener("click", () => {
    openGallery(item.dataset.gallery, item.dataset.index);
  });
});

closeBtn.addEventListener("click", closeGallery);
nextBtn.addEventListener("click", nextImage);
prevBtn.addEventListener("click", prevImage);

gallery.addEventListener("click", (e) => {
  if (e.target === gallery) closeGallery();
});

document.addEventListener("keydown", (e) => {
  if (!gallery.classList.contains("active")) return;

  if (e.key === "Escape") closeGallery();
  if (e.key === "ArrowRight") nextImage();
  if (e.key === "ArrowLeft") prevImage();
});
